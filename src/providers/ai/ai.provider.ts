function buildAnalysisPrompt(userPrompt: string, consistency: number): string {
  return `You are an investigative research assistant. Analyze this objective and create a structured plan.

Objective: ${userPrompt}

Search precision level: ${consistency}/10 (1=very broad, 10=extremely strict)

Respond ONLY with valid JSON:
{
  "problemAnalysis": "brief analysis of the problem",
  "strategy": "recommended investigation strategy",
  "targetAudiences": ["audience1", "audience2"],
  "suggestedSources": ["source1", "source2"],
  "expectedContactTypes": ["type1", "type2"]
}`
}

function buildMessagePrompt(contact: Contact, context: string): string {
  return `Generate professional outreach messages for this contact based on the investigation context.

Contact: ${contact.name}, ${contact.role} at ${contact.organization}
Specialization: ${contact.specialization}
Context: ${context}

Respond ONLY with valid JSON:
{
  "emailSubject": "subject line",
  "emailBody": "full email body",
  "contactFormMessage": "shorter contact form message",
  "followUpMessage": "follow-up message"
}`
}

function buildTargetDiscoveryPrompt(brief: CampaignBrief): string {
  const contactDesc =
    brief.contactType === 'corporate'
      ? 'companies and businesses'
      : brief.contactType === 'individual'
        ? 'individual professionals, freelancers and consultants'
        : 'NGOs, institutions, public organizations and associations'
  return `You are a professional web researcher with deep knowledge of online directories, industry portals, and business databases.

Your task: generate a list of 10-15 REAL, currently active websites where ${contactDesc} related to "${brief.affinityCategory}" (specifically "${brief.affinitySubcategory}") in "${brief.country}" can be found.

Campaign objective: ${brief.description}

REQUIREMENTS for each target URL:
- Must be a real, existing, publicly accessible website
- Must list or feature organizations/individuals with publicly visible contact info
- Should be relevant to ${brief.affinityCategory} / ${brief.affinitySubcategory} in ${brief.country}
- Include a mix of types: industry directories, sector portals, association registers, media outlets, blogs

IMPORTANT: Only include websites you are confident exist. Do not invent URLs.

Respond ONLY with valid JSON (no markdown, no explanation outside the JSON):
{
  "targets": [
    {
      "url": "https://example-directory.com/members",
      "domain": "example-directory.com",
      "rationale": "National membership directory for ${brief.affinitySubcategory} professionals in ${brief.country}",
      "type": "directory",
      "estimatedContacts": 15
    }
  ]
}`
}

function parseJSON<T>(text: string): T {
  const match = text.match(/\{[\s\S]*\}/)
  if (!match) throw new Error('No JSON found in response')
  return JSON.parse(match[0]) as T
}

// ── OpenAI Provider ──
function createOpenAIProvider(apiKey: string, model: string): AIProvider {
  async function call(messages: { role: string; content: string }[], plain = false) {
    const res = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json', Authorization: `Bearer ${apiKey}` },
      body: JSON.stringify({ model, messages, temperature: plain ? 0.9 : 0.7 }),
    })
    if (!res.ok) throw new Error(`OpenAI API error: ${res.status}`)
    const data = await res.json()
    return data.choices[0].message.content as string
  }

  return {
    async analyzeCampaign(brief) {
      try {
        const content = await call(
          [{ role: 'user', content: buildCampaignReportPrompt(brief) }],
          true,
        )
        return { success: true, data: { reportMarkdown: content } }
      } catch (e) {
        return { success: false, error: (e as Error).message }
      }
    },
    async analyzePrompt(prompt, consistency) {
      try {
        const content = await call([
          { role: 'user', content: buildAnalysisPrompt(prompt, consistency) },
        ])
        return { success: true, data: parseJSON<AIAnalysisResult>(content) }
      } catch (e) {
        return { success: false, error: (e as Error).message }
      }
    },
    async generateMessages(contact, context) {
      try {
        const content = await call([
          { role: 'user', content: buildMessagePrompt(contact, context) },
        ])
        return { success: true, data: parseJSON<AIMessageResult>(content) }
      } catch (e) {
        return { success: false, error: (e as Error).message }
      }
    },
    async generateSearchTargets(brief) {
      try {
        const content = await call([{ role: 'user', content: buildTargetDiscoveryPrompt(brief) }])
        return { success: true, data: parseJSON<TargetDiscoveryResult>(content) }
      } catch (e) {
        return { success: false, error: (e as Error).message }
      }
    },
    async testConnection() {
      try {
        const res = await fetch('https://api.openai.com/v1/models', {
          headers: { Authorization: `Bearer ${apiKey}` },
        })
        if (!res.ok) throw new Error(`Status ${res.status}`)
        return { success: true, data: { model } }
      } catch (e) {
        return { success: false, error: (e as Error).message }
      }
    },
    async evaluate(prompt) {
      try {
        const content = await call([{ role: 'user', content: prompt }])
        return { success: true, data: content }
      } catch (e) {
        return { success: false, error: (e as Error).message }
      }
    },
  }
}

