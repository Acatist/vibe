// ── Google AI (Gemini) Provider ──
function createGoogleProvider(apiKey: string, model: string): AIProvider {
  const base = 'https://generativelanguage.googleapis.com/v1beta'

  async function call(prompt: string) {
    const res = await fetch(`${base}/models/${model}:generateContent?key=${apiKey}`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ contents: [{ parts: [{ text: prompt }] }] }),
    })
    if (!res.ok) throw new Error(`Google AI error: ${res.status}`)
    const data = await res.json()
    return data.candidates[0].content.parts[0].text as string
  }

  return {
    async analyzeCampaign(brief) {
      try {
        const content = await call(buildCampaignReportPrompt(brief))
        return { success: true, data: { reportMarkdown: content } }
      } catch (e) {
        return { success: false, error: (e as Error).message }
      }
    },
    async analyzePrompt(prompt, consistency) {
      try {
        const content = await call(buildAnalysisPrompt(prompt, consistency))
        return { success: true, data: parseJSON<AIAnalysisResult>(content) }
      } catch (e) {
        return { success: false, error: (e as Error).message }
      }
    },
    async generateMessages(contact, context) {
      try {
        const content = await call(buildMessagePrompt(contact, context))
        return { success: true, data: parseJSON<AIMessageResult>(content) }
      } catch (e) {
        return { success: false, error: (e as Error).message }
      }
    },
    async generateSearchTargets(brief) {
      try {
        const content = await call(buildTargetDiscoveryPrompt(brief))
        return { success: true, data: parseJSON<TargetDiscoveryResult>(content) }
      } catch (e) {
        return { success: false, error: (e as Error).message }
      }
    },
    async testConnection() {
      try {
        const res = await fetch(`${base}/models?key=${apiKey}`)
        if (!res.ok) throw new Error(`Status ${res.status}`)
        return { success: true, data: { model } }
      } catch (e) {
        return { success: false, error: (e as Error).message }
      }
    },
    async evaluate(prompt) {
      try {
        const content = await call(prompt)
        return { success: true, data: content }
      } catch (e) {
        return { success: false, error: (e as Error).message }
      }
    },
  }
}

// ── Factory ──
export function createAIProvider(type: AIProviderType, apiKey: string, model: string): AIProvider {
  switch (type) {
    case 'openai':
      return createOpenAIProvider(apiKey, model)
    case 'grok':
      return createGrokProvider(apiKey, model)
    case 'google':
      return createGoogleProvider(apiKey, model)
  }
}
